<template>
  <div>
    <city-header></city-header>
    <city-search :cities="cities"></city-search>
    <city-list :cities="cities" :hot="hotCities" :alphabet="alphabet"></city-list>
    <city-alphabet :cities="cities" @selectAlphabet="selectAlphabet"></city-alphabet>
  </div>
</template>

<script>
import axios from 'axios'
import CityHeader from './CityHeader'
import CitySearch from './CitySearch'
import CityList from './CityList.vue'
import CityAlphabet from './CityAlphabet.vue'
export default {
  name: 'City',
  components: {
    CityHeader,
    CitySearch,
    CityList,
    CityAlphabet
  },
  data () {
    return {
      cities: {},
      hotCities: [],
      alphabet: ''
    }
  },
  methods: {
    getCityInfo () {
      axios.get('/api/city.json')
        .then(this.handleGetCityInfoSucc)
    },
    selectAlphabet (alphabet) {
      this.alphabet = alphabet
    },
    handleGetCityInfoSucc (res) {
      res = res.data
      if (res.ret && res.data) {
        const data = res.data
        this.cities = data.cities
        this.hotCities = data.hotCities
      }
    }
  },
  mounted () {
    this.getCityInfo()
  }
}
</script>

